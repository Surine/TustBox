public class Subject {
    private int id;
    private String subject_name;
    private String subject_intro;
    private String subject_image;
    private String subject_user;
    private String subject_time;
    private String subject_hot;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getSubject_name() {
        return subject_name;
    }

    public void setSubject_name(String subject_name) {
        this.subject_name = subject_name;
    }

    public String getSubject_intro() {
        return subject_intro;
    }

    public void setSubject_intro(String subject_intro) {
        this.subject_intro = subject_intro;
    }

    public String getSubject_image() {
        return subject_image;
    }

    public void setSubject_image(String subject_image) {
        this.subject_image = subject_image;
    }

    public String getSubject_user() {
        return subject_user;
    }

    public void setSubject_user(String subject_user) {
        this.subject_user = subject_user;
    }

    public String getSubject_time() {
        return subject_time;
    }

    public void setSubject_time(String subject_time) {
        this.subject_time = subject_time;
    }

    public String getSubject_hot() {
        return subject_hot;
    }

    public void setSubject_hot(String subject_hot) {
        this.subject_hot = subject_hot;
    }
}
